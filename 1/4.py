#libary 
import math
#For input 
num = float(input("Enter a number: "))
#For calculation 
cube = num ** 3
cube_root = num ** (1/3)
natural_log = math.log(num)
base_log_2 = math.log2(num)
power_6 = num ** 6
# To display output 
print(f"Cube: {cube}")
print(f"Cube Root: {cube_root}")
print(f"Natural Logarithm: {natural_log}")
print(f"Base-2 Logarithm: {base_log_2}")
print(f"Number raised to the power of 6: {power_6}")